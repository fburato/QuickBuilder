package com.example.beans;

public class BaseBean {

  private String foo;
  private String bar;

  public String getFoo() {
    return this.foo;
  }

  public void setFoo(final String foo) {
    this.foo = foo;
  }

  public String getBar() {
    return this.bar;
  }

  public void setBar(final String bar) {
    this.bar = bar;
  }

}

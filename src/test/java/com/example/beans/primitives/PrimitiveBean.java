package com.example.beans.primitives;

public class PrimitiveBean {

  private int     i;
  private long    l;
  private float   f;
  private double  d;
  private short   s;
  private char    c;
  private boolean b;
  private byte    by;

  public int getI() {
    return this.i;
  }

  public void setI(final int i) {
    this.i = i;
  }

  public long getL() {
    return this.l;
  }

  public void setL(final long l) {
    this.l = l;
  }

  public float getF() {
    return this.f;
  }

  public void setF(final float f) {
    this.f = f;
  }

  public double getD() {
    return this.d;
  }

  public void setD(final double d) {
    this.d = d;
  }

  public short getS() {
    return this.s;
  }

  public void setS(final short s) {
    this.s = s;
  }

  public char getC() {
    return this.c;
  }

  public void setC(final char c) {
    this.c = c;
  }

  public boolean isB() {
    return this.b;
  }

  public void setB(final boolean b) {
    this.b = b;
  }

  public byte getBy() {
    return this.by;
  }

  public void setBy(final byte by) {
    this.by = by;
  }

}
